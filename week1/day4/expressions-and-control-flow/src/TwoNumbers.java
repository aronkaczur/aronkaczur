public class TwoNumbers {
    public static void main(String[] args) {
        byte number1 = 22;
        byte number2 = 13;
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number2 * number1);
        System.out.println((double) number2 / number1);
        System.out.println(number1 / number2);
        System.out.println(number1 % number2);
    }
}

